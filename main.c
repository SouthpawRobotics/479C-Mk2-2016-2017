short autoMode = 0;

string autoModeNames[] = {"No autonomous", "15 Second Right", "15 Second Left", "60 Second Right", "60 Second Left"};

void driveForward(int speed){
	motor[backLeft] = speed;
	motor[backRight] = speed;
	motor[frontLeft] = speed;
	motor[frontRight] = speed;
}


// Positive values turn clockwise
void turn(int speed){
	motor[backLeft] = speed;
	motor[backRight] = -speed;
	motor[frontLeft] = speed;
	motor[frontRight] = -speed;
}

void lift(int speed){
	motor[topLeftLift] = speed;
	motor[bottomRightLift] = speed;
	motor[topRightLift] = speed;
}

void claw(int speed){
	motor[leftClaw] = speed;
	motor[rightClaw] = speed;
}

// Turns the winch to remove slack in preperation for lift
void removeSlackFronWinch(){
	if(winch3){
		if(!winch1){
			winch1 = true;
			motor[winch] = 127;
			resetMotorEncoder(winch);
		}
		if(getMotorEncoder(winch) >= 2565){
			motor[winch] = 0;
			winch1 = false;
			winch3 = false;
		}
	}

}

void climb() {
	winch2 = true;
	SensorValue[winchEncoder] = 0;
	motor[winch] = 127;
	untilEncoderCounts(14000, winchEncoder);
	motor[winch] = 0;
	winch2 = false;
}

void autonMenu(){
	bLCDBacklight = true;
	short index = 0;
	short buttons = 0;
	short lastButtons = 0;
	bool first = true;

	displayLCDCenteredString(0, autoModeNames[index]);
	displayLCDCenteredString(1, LCD_MENU_STRING);

	while((buttons != LCD_MID || first)  && bIfiRobotDisabled){
		buttons = nLCDButtons;
		first = false;
		writeDebugStreamLine("Buttons are : %i", buttons);
		if(buttons != 0 && lastButtons == 0){
			if(buttons == LCD_MID){
				autoMode = index;
				clearLCDLine(1);
			} else {
				if(buttons == LCD_LEFT){
					index--;
				} else if(buttons == LCD_RIGHT){
					index++;
				}
				if(index < 0) index = AUTO_COUNT - 1;
				if(index >= AUTO_COUNT) index = 0;
				clearLCDLine(0);
				displayLCDCenteredString(0, autoModeNames[index]);
			}
		}
		lastButtons = buttons;
	}
}

