task usercontrol()
{
  // User control code here, inside the loop

  while (true)
  {
    //holonomic drive*..
   	motor[frontLeft] = vexRT[Ch4] + vexRT[Ch1] + vexRT[Ch3];
   	motor[frontRight] = -vexRT[Ch4] - vexRT[Ch1] + vexRT[Ch3];
   	motor[backLeft] = -vexRT[Ch4] + vexRT[Ch1] + vexRT[Ch3];
   	motor[backRight] = vexRT[Ch4] -  vexRT[Ch1] + vexRT[Ch3];

   	if(vexRT(Btn5UXmtr2)){
	  	lift(127);
		} else if(vexRT(Btn5DXmtr2)){
			lift(-127);
		} else {
			lift(0);
		}

		if(vexRT(Btn6UXmtr2)){
			claw(127);
		} else if(vexRT(Btn6DXmtr2)){
			claw(-127);
		} else {
			claw(0);
		}

		if(vexRT(Btn7L) || vexRT(Btn8L) || vexRT(Btn7LXmtr2) || vexRT(Btn8LXmtr2)) {
			motor[winch] = 127;
		} else if(vexRT(Btn7D) && vexRT(Btn7DXmtr2)) {
			motor[winch] = -127;
		} else if(!winch3) {
			motor[winch] = 0;
		}

		if(vexRT(Btn7U) && vexRT(Btn7UXmtr2) && !winch1 && !winch2){
			winch3 = true;
		}

		if(vexRT(Btn8U) && vexRT(Btn8UXmtr2) && !winch1 && !winch2){
			climb();
		}
		removeSlackFronWinch();
  }
}
